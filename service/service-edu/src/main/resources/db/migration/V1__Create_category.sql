(
    `id`          char(19)    not null comment '课程分类id',
    `title`       varchar(10) not null comment '分类名称',
    `parent_id`   char(19)    not null default '0' comment '父级id',
    `sort`        int(10) unsigned not null default '0' comment '排序字段',
    `create_time` datetime    not null default now() comment '创建时间',
    `update_time` datetime on update now() comment '更新时间',
    primary key (`id`),
    key           `idx_parent_id` (`parent_id`)
) engine=innodb default charset=utf8mb4 row_format=compact comment='课程分类';

